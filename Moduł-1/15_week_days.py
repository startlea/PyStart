week_day = int(input("Give me number of the day of the week "))

if week_day == 1:
    print("It's monday")
elif week_day == 2:
    print("It's tuesday")
elif week_day == 3:
    print("It's wednesday")
elif week_day == 4:
    print("It's thursday")
elif week_day == 5:
    print("It's friday")
elif week_day == 6:
    print("It's saturday")
elif week_day == 7:
    print("It's sunday")
else:
    print("Wrong number please try again")